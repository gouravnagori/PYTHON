# Type convesion means we convert the data type of a variable in an another type
# Ex: int -> float
# Python's interpreter automatically do the type conversion

a = 2
b = 2.32
print(a+b) # here int is convert into float (superier value)
# 2.00 + 2.32 = 4.32

# TYPE CASTING
#If we want to convert the dat type of a variable manually then we use Type Casting
